    private void enableComponents() {
        if (rbtOutstandingReport.isSelected()) {
            rbtDaily.setEnabled(false);
            rbtWeekly.setEnabled(false);
            rbtMonthly.setEnabled(false);
            rbtDateRange.setEnabled(false);
            dateChooserDaily.setEnabled(false);
            monthChooserMonthly.setEnabled(false);
            dateChooserDateRangeEnd.setEnabled(false);
            dateChooserDateRangeStart.setEnabled(false);
        } else if (rbtSalesReport.isSelected()) {
            if (rbtDaily.isSelected()) {
                dateChooserDaily.setEnabled(true);
                monthChooserMonthly.setEnabled(false);
                dateChooserDateRangeEnd.setEnabled(false);
                dateChooserDateRangeStart.setEnabled(false);
            } else if (rbtWeekly.isSelected()) {
                dateChooserDaily.setEnabled(false);
                monthChooserMonthly.setEnabled(false);
                dateChooserDateRangeEnd.setEnabled(false);
                dateChooserDateRangeStart.setEnabled(false);
            } else if (rbtMonthly.isSelected()) {
                dateChooserDaily.setEnabled(false);
                monthChooserMonthly.setEnabled(true);
                dateChooserDateRangeEnd.setEnabled(false);
                dateChooserDateRangeStart.setEnabled(false);
            } else if (rbtDateRange.isSelected()) {
                dateChooserDaily.setEnabled(false);
                monthChooserMonthly.setEnabled(false);
                dateChooserDateRangeEnd.setEnabled(true);
                dateChooserDateRangeStart.setEnabled(true);
            }

            rbtDaily.setEnabled(true);
            rbtWeekly.setEnabled(true);
            rbtMonthly.setEnabled(true);
            rbtDateRange.setEnabled(true);
        }
    }

