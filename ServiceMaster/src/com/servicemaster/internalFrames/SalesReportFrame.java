    private void btnVeiwActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVeiwActionPerformed
        Date start_date = new Date();
        Date end_date = new Date();
        String reportName;

        if (rbtSalesReport.isSelected()) {
            reportName = "reports/SalesReport.jasper";
            if (rbtWeekly.isSelected()) {
                DateTime dateTime = new DateTime().minusDays(7);
                start_date = dateTime.toDate();
                end_date = new Date();

                System.out.println(start_date);
                System.out.println(end_date);
            } else if (rbtMonthly.isSelected()) {
                int month = monthChooserMonthly.getMonth();
                DateTime dateTime = new DateTime().withMonthOfYear(month + 1);

                start_date = dateTime.dayOfMonth().withMinimumValue().toDate();
                end_date = dateTime.dayOfMonth().withMaximumValue().toDate();

                System.out.println(start_date);
                System.out.println(end_date);
            } else if (rbtDateRange.isSelected()) {
                start_date = dateChooserDateRangeStart.getDate();
                end_date = dateChooserDateRangeEnd.getDate();

                System.out.println(start_date);
                System.out.println(end_date);
            } else {
                start_date = dateChooserDaily.getDate();
                end_date = dateChooserDaily.getDate();

                System.out.println(start_date);
                System.out.println(end_date);
            }
        } else if (rbtOutstandingReport.isSelected()) {
            reportName = "reports/OutsdandingReport.jasper";
        } else {
            reportName = "";
        }

        if (!reportName.isEmpty()) {
            try {
                JdbcConnection jbConnection = new JdbcConnection();
                Connection connection = jbConnection.getConnection();

                Map map = new HashMap();
                map.put("start_date", start_date);
                map.put("end_date", end_date);

                JasperPrint jasperPrint = JasperFillManager.fillReport(reportName, map, connection);
                if (jasperPrint.getPages().size() > 0) {
                    JasperViewer.viewReport(jasperPrint, false);
                } else {
                    InformationDialog.showMessageBox("No records found", "Not Found", this);
                }
            } catch (JRException ex) {
                Logger.getLogger(SalesReportFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnVeiwActionPerformed

