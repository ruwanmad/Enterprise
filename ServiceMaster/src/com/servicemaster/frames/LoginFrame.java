    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        SwingUtilities.invokeLater(new Runnable() {
            @Override
            public void run() {
                txtUsername.requestFocus();
            }
        });

        Calendar calendar = Calendar.getInstance();
        int hour = calendar.get(Calendar.HOUR);
        int am_pm = calendar.get(Calendar.AM_PM);

        if (am_pm == 0) {
            lblGreeting.setText("Good Morning...!");
        } else if (am_pm == 1) {
            if (hour >= 4) {
                lblGreeting.setText("Good Evening...!");
            } else {
                lblGreeting.setText("Good Afternoon...!");
            }
        }
    }//GEN-LAST:event_formWindowOpened

    private void login() {
        String username = txtUsername.getText().trim();
        String password = new String(pwdPassword.getPassword()).trim();
        if (username.isEmpty() || password.isEmpty()) {
            InformationDialog.showMessageBox("Please enter valid details", "Invalid", null);
        } else {
            try {
                Session session = HibernateUtil.getSessionFactory().openSession();
                session.beginTransaction();

                User user = (User) session
                        .createCriteria(User.class)
                        .add(Restrictions.eq("userName", username))
                        .add(Restrictions.eq("password", password))
                        .uniqueResult();

                if (user != null) {
                    UserPrivilage userPrivilage = user.getUserPrivilage();
                    MainFrame mainFrame = new MainFrame(user);
                    mainFrame.setExtendedState(JFrame.MAXIMIZED_BOTH);

                    EnableFeatures enableFeatures = new EnableFeatures();
                    enableFeatures.enableFeature(userPrivilage, mainFrame);
                    mainFrame.setVisible(true);
                    this.dispose();
                } else {
                    InformationDialog.showMessageBox("<html>Invalid username or password.<br>Please try again.<html>", "Invalid", null);
                }

                session.getTransaction().commit();
                session.close();
            } catch (HibernateException | NullPointerException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                LOGGER.error(ex);
            }
        }

        SwingUtilities.invokeLater(new Runnable() {
            @Override
            public void run() {
                txtUsername.requestFocus();
            }
        });
    }

